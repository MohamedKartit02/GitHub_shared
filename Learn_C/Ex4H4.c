/* This program print the scientific and flaoting format of 123.456*/

#include <stdio.h>
int main()
{
	float dbl_num = 123.456;

	printf("this is the scientific value of the number 123.456 = %e.\n", dbl_num);
	printf("This is the floating point value of the number 123.456 %f.\n", dbl_num);

	return 0;
}
